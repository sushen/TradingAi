def time_for_one_candle(total_candles, total_time):
    time_per_candle = total_time / total_candles
    return time_per_candle

# Example usage
total_candles = 43200
total_time = 201
time_per_candle = time_for_one_candle(total_candles, total_time)
print(f"Time for 1 candle: {time_per_candle} seconds")

